    public boolean TambahBuku(){
        String id_buku = jTextField_idbuku.getText();
        String judul_buku = jTextField_judulbuku.getText();
        String pengarang = jTextField_pengarang.getText();
        String penerbit = jTextField_penerbit.getText();
        String stok = jTextField_stok.getText();
        
        if(id_buku.trim().equals("") || judul_buku.trim().equals("") || pengarang.trim().equals("") || penerbit.trim().equals("") || stok.trim().equals("") ){
            JOptionPane.showMessageDialog(null, "Isi semua data!", "Data Kosong!", 2);
            return false;
        }else{
            return true;
        }
    }
    
