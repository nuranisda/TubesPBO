    public boolean cekIdBuku(String id_buku){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        
        String query = "SELECT * FROM `data_buku` WHERE `id_buku` = ?";
        
        try{
            st = LoginToRegister.getConnection().prepareStatement(query);
            st.setString(1, id_buku);
            rs = st.executeQuery();
            
            if(rs.next()){
                username_exist = true;
                JOptionPane.showMessageDialog(null, "Id Buku ini telah tersedia, silahkan masukkan id lain!", "ID gagal", 2);
            }
        } catch (SQLException ex){
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return username_exist;
    }
    
    private void jButton_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_tambahActionPerformed
        // TODO add your handling code here:
        PreparedStatement st;
        ResultSet rs;

        String judul_buku = jTextField_judulbuku.getText();
        String stok = jTextField_stok.getText();
        String id_buku = jTextField_idbuku.getText();
        String penerbit = jTextField_penerbit.getText();
        String pengarang = jTextField_pengarang.getText();
        
        if(TambahBuku()){
            if(!cekIdBuku(id_buku)){
                PreparedStatement ps;
                //ResultSet rs;

                String registerUserQuery = "INSERT INTO `databuku`(`id_buku`,`judul_buku`,`penerbit`,`pengarang`,`stok`) VALUES (?,?,?,?,?)";

                try {
                    ps = LoginToRegister.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1, id_buku);
                    ps.setString(2, judul_buku);
                    ps.setString(3, pengarang);
                    ps.setString(4, penerbit);
                    ps.setString(5, stok);

                    if(ps.executeUpdate() != 0){
                        JOptionPane.showMessageDialog(null, "Penambahan Data Buku Berhasilt!");
                    }else{
                        JOptionPane.showMessageDialog(null, "Error: Harap cek kembali");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jButton_tambahActionPerformed

